class Layout extends React.Component {
  constructor(props) {
    super(props)

    this.state = {
      gameSocket: {},
      board: emptyBoard.slice(0),
      player: {
        name: 'playerName: yoyo',
        score: 0,
        direction: 'right',
        x: 1,
        y: 1
      }
    }
  };

  componentDidMount() {
    this.createGameSocket()
  }

  handleGameData = response => {
    console.log(response, 'gameData response ...********')
  };

  createGameSocket() {
    let cable = Cable.createConsumer(WEBSOCKET_HOST)
    let gameSocket = cable.subscriptions.create({ channel: 'GameDataChannel' },
    {
      connected: () => {},
      received: (gameData) => this.handleGameData(gameData),
      create: function(gameEvent) {
        this.perform('create', {
          gameEvent: gameEvent
        });
      }
    });
    this.setState({gameSocket: gameSocket})
  };

  sendGameEvent = (e) => {
    this.state.gameSocket.create('here is a game event from the client!')
  };

  updatePlayerCoordinates = (x, y) => {
    this.setState({
      player: {...this.state.player, x: x, y: y}
    });
  };

  updatePlayerScore = (score) => {
    this.setState({
      player: {...this.state.player, score: score}
    });
  };

  updateBoard = (x, y, value) => {
    // this.setState({
    //   board:
    // });
    this.state.board[x][y] = value
  };

  render = () => {
    return (
      <div className="layout">
        <SvgBoard/>
        <h2 onClick={this.sendGameEvent}>Pacman</h2>
        <div className='game'>
          <Score player={this.state.player} />
          <Board
            board={this.state.board}
            player={this.state.player}
            updatePlayerCoordinates={this.updatePlayerCoordinates}
            updatePlayerScore={this.updatePlayerScore}
            updateBoard={this.updateBoard}
          />
          </div>
      </div>
    );
  };
}

export default Layout;
